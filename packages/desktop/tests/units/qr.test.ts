const toBits = (matrix: boolean[][]) => matrix.map((row) => row.map((cell) => (cell ? '1' : '0')).join('')).join('\n');

const cell = (matrix: boolean[][], x: number, y: number) => matrix[y]?.[x] ?? false;

describe('qr generator', () => {
  it('reproduces the pairing payload matrix bit for bit', () => {
    expect(toBits(qrMatrix(payload, 'medium'))).toBe(golden);
  });

  it('produces a square matrix sized to the chosen version', () => {
    const matrix = qrMatrix(payload, 'medium');
    expect(matrix.length).toBe(45);
    expect(matrix.every((row) => row.length === matrix.length)).toBe(true);
  });

  it('draws the three finder patterns', () => {
    const matrix = qrMatrix('start', 'medium');
    const size = matrix.length;
    const finderCorner = (originX: number, originY: number) =>
      cell(matrix, originX, originY) &&
      cell(matrix, originX + 6, originY) &&
      cell(matrix, originX, originY + 6) &&
      !cell(matrix, originX + 1, originY + 1);
    expect(finderCorner(0, 0)).toBe(true);
    expect(finderCorner(size - 7, 0)).toBe(true);
    expect(finderCorner(0, size - 7)).toBe(true);
  });

  it('selects a larger version as the payload grows', () => {
    expect(qrMatrix('start', 'medium').length).toBeLessThan(qrMatrix('start'.repeat(40), 'medium').length);
  });

  it('renders an svg with a quiet-zone viewBox and rounded modules', () => {
    const svg = qrSvg(payload, { ecc: 'medium', margin: 4 });
    expect(svg).toContain('viewBox="0 0 53 53"');
    expect(svg).toContain('<rect');
    expect(svg).toContain('width="7" height="7" rx="1.85"');
    expect(svg).toContain('width="0.82" height="0.82" rx="0.3"');
    expect(svg).toContain('width="3" height="3" rx="1"');
    expect(svg).toContain('style="--qr-branch:');
  });
});
